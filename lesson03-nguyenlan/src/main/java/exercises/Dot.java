package exercises;

public class Dot {
	private double x;
	private double y;
	
	public Dot() {
		
	}
	public Dot(double x, double y) {
		this.x = x;
		this.y = y;
	}
	public double getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}	
}
