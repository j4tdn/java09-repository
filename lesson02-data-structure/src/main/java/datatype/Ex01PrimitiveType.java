package datatype;

public class Ex01PrimitiveType {
	private static int f=10;	//global variable, attribute
	
	public static void main(String[] args) {
		//local variable, no access modifier
		int a=10;
		float b=5.2f;
		double c=3.3d;
		char d='d';
		boolean e = true;
		System.out.println("a ="+a);
		
	}
}
