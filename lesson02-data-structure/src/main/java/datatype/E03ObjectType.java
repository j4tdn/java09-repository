package datatype;

public class E03ObjectType {
public static void main(String[] args) {
	//apply with available object types of standard Java libs
	Integer a=10;
	Integer aa=new Integer(12);
	Float b=5.2f;
	Double c=2.4d;
	Character d='a';
	Boolean e=true;
	System.out.println("a:"+a);
	System.out.println("aa:"+aa);
	
	String f="Hello";
	String g=new String("Hello");
}
}
