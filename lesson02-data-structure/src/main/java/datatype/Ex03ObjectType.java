package datatype;

public class Ex03ObjectType {
	public static void main(String[] args) {
		// Apply with available object types of standard JAVA libs
		Integer a = 12;
		Integer aa = new Integer(12);
		Float b = 5.2f;
		Double c = 5.3d;
		Character d = 'd';
		Boolean e = true;
		String f = "Hello";

		System.out.println("a: " + a);
		System.out.println("aa: " + aa);
	}
}
