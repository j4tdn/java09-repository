package datatype;

public class Ex03ObjectType {

	public static void main(String[] args) {
		//apply with available object types of standard Java libs
		Integer a=10;
		Integer aa=new Integer(12);
		System.out.println(a+","+aa);
		Float b=5.2f;
		Double c=3.3d;
		Character d='D';
		Boolean e=true;
		
		String f="Hello";
		String g=new String("Hello");
	}
}
